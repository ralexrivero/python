#!/usr/bin/env python

'''
  operadores matematicos
  + suma
  - resta
  * multiplicacion
  / division
  // division entera
  % modulo
  ** potencia
'''

print(2 + 1)
print(2 - 1)
print(2 * 1)
print(5 / 2)
print(5 // 2)
print(5 % 2)
print(5 ** 3)

'''
  aplicado a cadenas de texto
'''

print('texto1' + 'texto2')
print('texto1' *3)

'''
  operadores logicos
  > mayor
  < menor
  <= menor o igual
  >= mayor o igual
  == igual
  != diferente
'''

print(1 > 2)
print(1 < 2)
print(1 <= 2)
print(1 >= 2)
print(1 == 2)
print(1 != 2)
